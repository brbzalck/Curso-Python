"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""
contador = 0 

while contador <= 10: # > temos True, Enquanto a condição for verdadeira o While trabalha
    contador = contador + 1 # no caso vai contar de um até 11
    print(contador)

print('Acabou')